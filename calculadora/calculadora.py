#calculadora sem construtor
class Calculadora(object):

    def __init__(self):
        pass

    def soma(self, a ,b):
        return a +b

    def subtrai(self,a,b):
        return a-b


    def multiplica(self,a,b):
        return a*b


    def divide(self,a,b):
        return a/b

                    
